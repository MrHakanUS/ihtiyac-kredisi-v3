                                <p><strong>Ekonomist Prof. Dr. Ahmet Yılmaz'dan:</strong> “Teknik tavsiyem: Yapılandırma teklifini alınca, efektif yıllık maliyet oranına (EYMO) mutlaka bakın. Bu, tüm masraflar dahil gerçek maliyeti gösterir. İki banka teklifini karşılaştırırken aylık taksite değil, EYMO'ya bakın. Ayrıca, gelirinizin en fazla %40'ını borç taksitlerine ayırın. 60 ay çok uzun, bu sürede geliriniz artabilir. Artarsa, ek ödemeler yaparak borcu erken kapatma yoluna gidin. Faizden kurtulursunuz.”</p>
