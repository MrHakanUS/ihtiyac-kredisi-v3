                                    <p>"Kredi kullanımı artık bir tabu olmaktan çıktı, neredeyse bir sosyal norm haline geldi. Ancak bireyler, toplumsal baskıyla 'prestij tüketimi' için borçlanabiliyor. Örneğin, düğün kredisi. Bir araştırmamızda, düğün masraflarının ortalama %40'ının krediyle finanse edildiğini gördük. Bu kararı verirken heyecanlı ve bir o kadar da tedirgin olabilirsiniz, bu çok normal. Ama sizden ricam, bu sosyal faizi de hesaplamanıza dahil edin. 'Bu kredi olmasa da evlenebilir miydik?' sorusunu bir kere kendinize sorun."</p>
