                            <section id='sik-sorulan-sorular'>
                                <h2>Sık Sorulan Sorular (SSS)</h2>

                                <p><strong>S: TEB kredi kartı başvurusu için gelir şartı var mı? Öğrenciyim, başvurabilir miyim?</strong></p>
                                <p>C: Evet, gelir şartı var. Ancak bu, kart türüne göre değişiyor. Classic kartlar için asgari gelir şartı daha düşükken, Platinum ve World için daha yüksek. Öğrenciler için "Öğrenci Kredi Kartı" veya düşük limitli Classic kart seçenekleri mevcut. Öğrenci belgenle başvurabilirsin, ancak limitin daha düşük olacaktır muhtemelen.</p>

                                <p><strong>S: Başvurum reddedildi. Sebebi ne olabilir ve ne yapmalıyım?</strong></p>
                                <p>C: En yaygın sebep düşük kredi notu. Findeks veya KKB skorun 1400'ün altındaysa reddedilme ihtimalin yüksek. Diğer nedenler: gelir beyanının yetersiz görülmesi, mevcut borç yükünün fazla olması, kredi geçmişinde gecikme kaydı olması. Yapılacak en doğru şey, 3 ay bekleyip kredi notunu düzeltmeye çalışmak (küçük tutarlı kredileri zamanında ödeyerek) ve sonra yeniden başvurmak.</p>

                                <p><strong>S: Kredi kartımı aldım ama limit beklentimin altında. Limit artırımı talep edebilir miyim?</strong></p>
                                <p>C: Kesinlikle. Genellikle kartı düzenli kullanıp ödemelerini aksatmadan yaptığın 6 ay sonra otomatik limit artışı teklifi gelebilir. Ya da sen TEB Mobil'den veya müşteri hizmetlerini arayarak "limit artırım talebi" oluşturabilirsin. Banka gelir durumunu tekrar sorgulayıp yeni bir değerlendirme yapar.</p>

                                <p><strong>S: TEB kredi kartı ile yurtdışında alışveriş yapabilir miyim? Komisyon oranı nedir?</strong></p>
                                <p>C: Yapabilirsin. Visa veya MasterCard ağına bağlı olduğu sürece dünyanın her yerinde kullanılabilir. Döviz işlemlerinde genellikle döviz alış kurunun üzerine %1-2 civarında bir "döviz işlem komisyonu" alınır. Bu oran kartın türüne göre değişir, bazı premium kartlarda sıfır komisyon olabilir. Kartını almadan önce sözleşmeden bu oranı kontrol et.</p>

                                <p><strong>S: Birden fazla kredi kartım olursa kredi notum düşer mi?</strong></p>
                                <p>C: Doğrudan düşürmez. Hatta, birden fazla krediyi düzgün yönetmek kredi notunu olumlu etkileyebilir. Ancak, bu kartların toplam limiti yüksekse ve sen çok yüksek harcamalar yaparsan, "kullanılabilir limitin azalması" risk algını artırabilir ve yeni bir kredi başvurusunda bu dikkate alınabilir. Yani sayı değil, bu kredileri nasıl yönettiğin önemli.</p>
                            </section>

