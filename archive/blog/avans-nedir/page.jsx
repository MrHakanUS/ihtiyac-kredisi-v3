                            <section>
                                <h2>Sık Sorulan Sorular (SSS)</h2>

                                <h3>Avans kredisi ile ihtiyaç kredisi arasındaki fark nedir?</h3>
                                <p>Avans, genelde mevcut bir limitin (kredi kartı, maaş) öne alınmasıdır, daha hızlıdır, daha düşük tutarlıdır ve genelde daha yüksek faizlidir. İhtiyaç kredisi ise bankaya ayrıca başvurup, belgelerinizi sunup, belirli bir amaç için (evlilik, tatil, tadilat) aldığınız, daha uzun vadeli ve daha düşük faizli (genelde) bir kredidir. Kullanım alanı daha geniştir.</p>

                                <h3>Avans kredisi faiz oranları 2025'te ne kadar?</h3>
                                <p>2025 Aralık itibarıyla, aylık faiz oranları %1.8 ile %3.5 arasında değişiyor. Yıllık bazda ise bu, %21.6 ile %42 aralığına denk geliyor. Devlet bankaları genellikle daha uygun oranlar sunuyor.</p>

                                <h3>Avans kredisi çekmek kredi notunu düşürür mü?</h3>
                                <p>Başvuru sırasında yapılan sorgu kısa süreliğine notunuzu bir iki puan düşürebilir. Ancak asıl risk, ödemeleri aksatmanız durumunda ortaya çıkar. Düzenli ödeme yaparsanız, notunuz zamanla toparlanır ve hatta düzenli ödeme geçmişiniz sayesinde yükselebilir bile.</p>

                                <h3>Maaş avansı nedir, nasıl alınır?</h3>
                                <p>Maaş avansı, işvereninizden, henüz hak etmediğiniz ama edeceğiniz maaşınızın bir kısmını peşin olarak talep etmektir. Faizsizdir genelde. İnsan kaynakları departmanınıza veya muhasebenize yazılı veya sözlü başvurmanız gerekir. Her şirket vermeyebilir veya belirli koşulları olabilir.</p>

                                <h3>Avans kredisi başvurusu için gerekli belgeler nelerdir?</h3>
                                <p>Online başvurularda genellikle ek belge istenmez, banka sizin mevcut bilgilerinizi kullanır. Ancak yeni bir bankadan yüksek tutarlı bir avans (kredi avansı) talep ediyorsanız, kimlik, ikametgah ve gelir belgesi (maaş bordrosu, SGK dökümü) istenebilir. Mevcut müşteriyseniz süreç daha kolay.</p>
                            </section>

