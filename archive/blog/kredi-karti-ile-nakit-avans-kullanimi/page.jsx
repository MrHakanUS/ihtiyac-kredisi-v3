                                    <li><strong>Kredi Kartı Sigortası Kapsam Dışı:</strong> Birçok kredi kartı, hayat sigortası veya işsizlik sigortası gibi ek hizmetler sunar. Ancak bu sigortalar çoğunlukla <em>nakit avans borçlarını kapsamaz</em>. Hastalık veya işsizlik durumunda, normal alışveriş borcunuz sigorta tarafından ödenebilirken, nakit avans kısmı size kalır.</li>
