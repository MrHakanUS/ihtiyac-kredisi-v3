                            <section id='sik-sorulan-sorular'>
                                <h2>İhtiyaç Kredisi Hakkında Sık Sorulan Sorular (SSS)</h2>

                                <p><strong>S: Kredi notum düşük, yine de asgari tutarda kredi alabilir miyim?</strong><br />
                                C: Aldığınız tutar düşük olsa da kredi notunuz çok düşükse (örneğin 1000 altı) bankalar risk almayabilir. Öncelikle kredi notunuzu yükseltmeye çalışın.</p>

                                <p><strong>S: Asgari tutardan daha az bir krediye ihtiyacım var. Ne yapmalıyım?</strong><br />
                                C: Bankaların bireysel finansman/taşıt kredisi gibi ürünleri veya kredi kartı nakit avansları daha düşük limitli olabilir. Ya da aile içi finansmanı düşünebilirsiniz. Kredi çekmek her zaman tek çözüm değil.</p>

                                <p><strong>S: Birden fazla bankaya aynı anda başvuru yapmak doğru mu?</strong><br />
                                C: Hayır! Kısa süre içinde çok sayıda "hard query" (kesin başvuru) kredi notunuzu hızla düşürür. Önce ön onay alın, sonra en uygun 1-2 bankaya resmi başvuru yapın.</p>

                                <p><strong>S: Asgari tutar ve faiz oranları ne sıklıkla değişir?</strong><br />
                                C: Faiz oranları piyasa koşullarına, TCMB politikalarına ve bankaların kampanyalarına bağlı olarak günlük bile değişebilir. Asgari tutarlar ise daha seyrek değişir, genellikle üç ayda bir veya yılda bir gözden geçirilir.</p>

                                <p><strong>S: İhtiyaç kredisi çekmek konut kredisi başvurumu etkiler mi?</strong><br />
                                C: Evet, etkiler. Mevcut kredinizin aylık taksiti, konut kredisi için hesaplanan geri ödeme oranınızı (GÖO) düşürür. Yani daha az konut kredisi çekebilirsiniz veya başvurunuz reddedilebilir.</p>
                            </section>

