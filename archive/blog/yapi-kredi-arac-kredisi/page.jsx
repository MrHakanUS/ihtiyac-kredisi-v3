                                    <p>Her bankanın teklifini bu tabloya doldurun. Sadece rakamlara değil, "esneklik" gibi subjektif kriterlere de puan verin. En yüksek puanı alan banka, muhtemelen sizin için en iyi seçimdir.</p>
