                                    <p>Türkiye Finans bir katılım bankası olduğu için faizsiz finansman prensipleriyle çalışır. Kar/zarar ortaklığı modeli kullanır. Bu da faiz hassasiyeti olanlar için tercih sebebi. Ancak yine de ürünün detaylarını iyice okuyun, çünkü bazı masraflar faiz gibi görünebilir.</p>
