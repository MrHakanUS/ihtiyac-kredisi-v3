                                <p>Bir de şu var: Faiz oranlarının gelecek seyri. Ekonomistler, 2026'da Fed'in (Amerikan Merkez Bankası) faiz indirim sürecine devam edebileceğini öngörüyor. Bu, dolar faizlerinin daha da düşebileceği anlamına gelebilir. Yani belki de şimdi 12 aylık sabit bir vadeyle kilitlenmek, ileride faizler düştüğünde avantajlı olabilir. Tam tersi de olabilir tabii, bu bir tahmin. Ama genel kural: Faizlerin düşeceğini düşünüyorsanız, uzun vadeye gitmek; yükseleceğini düşünüyorsanız, kısa vadeyle oynamak mantıklı olabilir. Karar sizin.</p>

