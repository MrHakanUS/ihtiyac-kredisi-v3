                                    <p>Geçen ay bir dostum aradı, "Araba aldım, eksperi filan atlattım ama şu son 15 bin TL'lik eksiklik için ihtiyaç kredisi çekeceğim, hangi banka iyi?" diye. Önce sordum: "Araba senin için gerçek bir ihtiyaç mıydı yoksa 'artık zamanı geldi' hissi mi?" Cevap veremedi. Sonra birlikte hesapladık, 15 bin TL'lik kredi 36 ayda toplam 16 bin 200 TL'ye mal olacaktı. "Peşinatı 15 bin TL daha artırsan, araba fiyatını 15 bin TL aşağı çekmeyi denesen olmaz mı?" dedim. Sonuç? Arabayı 15 bin TL daha ucuza aldı ve kredi çekmedi. Bazen çözüm daha fazla borçlanmak değil, mevcut planı yeniden düşünmektir.</p>
