                                <p>Evet, tüm bu analizlerden sonra gelelim can alıcı soruya: Yabancı bankalarda mevduat yapmak mantıklı mı? Cevap: Duruma göre değişir. Eğer sadece en yüksek faizi arıyorsanız, yerel bankaları da karşılaştırmalısınız. Ama yabancı bankaların sunduğu global hizmetler, döviz cinsinden ürün çeşitliliği ve belki de daha stabil bir kurumsal yapı sizin için önemliyse, o zaman değerlendirilebilir.</p>

