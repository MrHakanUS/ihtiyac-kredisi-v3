                                <p>Son olarak, faiz oranları pazarlık yapılabilecek bir şey değil genelde ama özellikle yüksek tutarlarda, banka temsilcinizle konuşup daha iyi bir oran isteyebilirsiniz. Bazen gizli kampanyalar olabiliyor. Bunu da unutmayın.</p>
