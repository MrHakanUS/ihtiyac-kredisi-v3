                            <section>
                                <h2>Sık Sorulan Sorular (SSS)</h2>

                                <h3>TOKİ İlk Evim ve ihtiyaç kredisi birlikte kullanılır mı?</h3>
                                <p>
                                    Evet, peşinatı tamamlamak için bankalardan <strong>ihtiyaç kredisi</strong> çekebilirsiniz. Ancak bu, aylık ödemenize ek bir yük getirir. İki kredinin toplam taksitini ödeyebileceğinizden emin olun.
                                </p>

                                <h3>Başvurduğum projeyi beğenmezsem vazgeçebilir miyim?</h3>
                                <p>
                                    Kura size çıktıktan sonra, sözleşme imzalamadan önce vazgeçme hakkınız var genellikle. Ama başvuru ücreti iade edilmez. Sözleşme imzalandıktan sonra vazgeçmek ciddi cezai şartlara yol açabilir, dikkat.
                                </p>

                                <h3>TOKİ evlerinde kira öder gibi oturup, sonra satma imkanı var mı?</h3>
                                <p>
                                    Hayır, öyle bir şey yok. TOKİ İlk Evim bir sosyal konuttur, amacı konut sahibi yapmaktır. Tapu, genellikle tüm ödemeler bitince veya belli bir yüzdesi ödenince verilir. Tapuyu aldıktan sonra satabilirsiniz, ama erken satışlarda TOKİ'ye bir ceza ödemeniz gerekebilir. Şartnameyi okumak şart.
                                </p>

                                <h3>Gelirim bir yıl sonra şartı aşarsa, evi geri alırlar mı?</h3>
                                <p>
                                    Başvuru anındaki geliriniz önemli. Sonradan gelirinizin artması, genellikle elinizden alınmasına sebep olmaz. Ama başvuru anında yanlış beyanda bulunursanız, tespit edildiğinde sözleşme feshedilir ve ödedikleriniz iade edilmez. Daima dürüst olun.
                                </p>
                            </section>

