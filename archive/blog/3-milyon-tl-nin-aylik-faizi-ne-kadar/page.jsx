                            <section id='sik-sorulan-sorular'>
                                <h2>3 Milyon TL ve Faiz ile İlgili Sık Sorulan Sorular</h2>

                                <h3>1. 3 milyon TL'yi hangi bankaya yatırsam en çok faiz alırım?</h3>
                                <p>Bu, anlık olarak değişir. Genelde katılım bankaları ve bazı ikincil ticari bankalar daha yüksek faiz verebiliyor. Ancak, bankanın güvenilirliği ve mevduat sigortası kapsamı (100.000 TL'ye kadar devlet güvencesi) da önemli. 3 milyon TL için güvenceli bir şekilde dağıtmak (farklı bankalara bölmek) akıllıca olabilir.</p>

                                <h3>2. 3 milyon TL ihtiyaç kredisi çekebilir miyim? Şartları neler?</h3>
                                <p>Çekebilmeniz için düzenli ve yeterli gelirinizin olması, kredi notunuzun yüksek olması gerekir. Bankalar bu tutarı çoğu zaman maaş bordrosu, SGK kaydı ve teminat isteyerek verir. Serbest meslek sahipleri için vergi levhası ve düzenli gelir belgesi gerekli.</p>

                                <h3>3. Faiz geliri vergisi (stopaj) dışında başka vergi var mı?</h3>
                                <p>Mevduat faizi için sadece %10 stopaj kesilir, gelir vergisi beyannamesi ile ayrıca bir ödeme yapmanız gerekmez. Ancak, kira geliri, borsa kazancı gibi diğer yatırım araçları farklı vergilendirilir.</p>

                                <h3>4. 3 milyon TL ile aylık 50.000 TL net gelir garanti mi?</h3>
                                <p>Hayır, garanti değil. Faiz oranları Merkez Bankası kararları, enflasyon ve piyasa koşullarına göre değişir. Bugün %25 olan faiz, 3 ay sonra %20'ye düşebilir. Getiriniz de düşer.</p>

                                <h3>5. Bu kadar parayla neden ihtiyaç kredisi çekeyim ki? Mevduat yapayım.</h3>
                                <p>İyi bir soru. Eğer paranız yoksa ve yatırım yaparak bu kredinin faizinden daha fazla getiri elde edeceğinize inanıyorsanız kredi çekmek mantıklı olabilir. Örneğin, krediyi işinize makine almak için çekip, makinenin size aylık 200.000 TL ek kazanç sağlayacağından eminseniz, 130.000 TL taksit ödemek mantıklı. Ama tüketim için çekmek genelde kötü bir fikirdir.</p>
                            </section>

