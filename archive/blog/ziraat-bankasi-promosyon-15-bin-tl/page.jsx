                                <p>Benim şahsi önerim şu: Önce acil ihtiyaç mı, yoksa ertelenebilir bir istek mi olduğuna karar verin. Sonra, en az 3 farklı bankanın (Ziraat, Halkbank, bir özel banka) teklifini alın. Toplam geri ödeme tutarlarını karşılaştırın. Ve en önemlisi, aylık bütçenize sadık kalarak, asla gelirinizin %40’ını aşan bir taksite imza atmayın. Unutmayın, bugünün 15 bin TL’si, yarının 20 bin TL geri ödemesi olabilir. Hazırlıklı olun.</p>
