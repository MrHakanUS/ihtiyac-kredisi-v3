                                    <li><strong>Kredi Notunu Kontrol Et:</strong> İlk iş Findeks veya banka uygulamalarından kredi notunuzu öğrenin. 1500 ve üzeri iyi kabul edilir. Notunuz düşükse, önce onu yükseltmenin yollarını araştırın (kredi kartı borcunu kapatmak, fatura ödemelerini düzene sokmak gibi).</li>
