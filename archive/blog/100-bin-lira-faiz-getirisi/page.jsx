                                    <li><strong>Resmi Başvuru ve Belgelerin Teslimi:</strong> En uygun gördüğünüz bankaya giderek (veya online) resmi başvurunuzu yapın. Gerekli belgeleri sunun. Banka, değerlendirmesini genelde 1-3 iş günü içinde yapar.</li>
