                                    <li><strong>Kesin Onay ve Sözleşme:</strong> Tüm belgeler tamamlandıktan ve ekspertiz raporu bankaya ulaştıktan sonra, kredinizin kesin onayı çıkar. Bu genelde birkaç iş günü sürer. Onay çıktıktan sonra, şubeye çağrılırsınız ve kredi sözleşmesini imzalarsınız. Sözleşmeyi dikkatlice okuyun! Özellikle erken ödeme cezaları, sigorta şartları gibi maddelere bakın.</li>
