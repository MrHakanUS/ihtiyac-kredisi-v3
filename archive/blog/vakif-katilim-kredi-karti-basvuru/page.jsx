                                    <p>Öncelikle reddin nedenini öğrenmeye çalışın. Banka bazen açıklama yapmayabilir. Kredi notunuzu ve gelir-borç oranınızı tekrar gözden geçirin. 3-6 ay arası düzenli gelir gösterebileceğiniz bir süre bekleyip, borçlarınızı azaltıp tekrar deneyin.</p>

