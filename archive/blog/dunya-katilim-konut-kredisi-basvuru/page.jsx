                                <p>Benim size kişisel önerim şu: Acele etmeyin. En az 3 farklı katılım bankasından (Kuveyt Türk, Albaraka, Ziraat Katılım gibi) detaylı teklif alın. Her birinin verdiği toplam maliyet tablosunu (kâr payı + tüm masraflar) yan yana koyun. Sadece aylık taksite değil, 10 yıl sonra toplamda ne kadar ödeyeceğinize bakın. Ve en önemlisi, sözleşmeyi <em>anlayarak</em> imzalayın. Anlamadığınız her maddeyi sorun.</p>

