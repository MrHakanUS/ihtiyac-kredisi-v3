                                <p><strong>S: Konut kredisi için dosya masrafı ne kadar?</strong><br />
                                C: Dosya masrafı, kredi tutarının genellikle %1-2'si civarındadır ama bankalar bunun için bir üst sınır (örneğin 5.000 TL) belirler. 2025'te İş Bankası'nda bu masraf, kampanyalarla sıfırlanabiliyor veya indirilebiliyor. Kesin tutarı başvuru sırasında öğrenin.</p>

