                                    <li><strong>Diyelim ki 50.000 TL’m var, Garanti’de günlük faiz mi yoksa ihtiyaç kredisi mi daha mantıklı?</strong> Bu soru çok yanlış aslında çünkü ikisi farklı şeyler. Eğer harcama yapmayacaksanız faiz hesabı, acil nakit ihtiyacınız varsa ve birikiminizi bozmak istemiyorsanız ihtiyaç kredisi düşünülebilir. Ama kredi faizi genelde daha yüksek olduğu için, birikimden harcama yapmak çoğu zaman daha mantıklı.</li>
