                                <p>Benim naçizane tavsiyem de şu: Kredi başvurusu yapmadan önce mutlaka kredi notunuzu öğrenin. Findeks veya KKB'den ücretsiz veya çok ucuza öğrenebilirsiniz. Notunuz düşükse, önce onu yükseltmeye çalışın. Küçük tutarlı kredileri zamanında ödeyerek notunuzu artırabilirsiniz. Bu, size çok daha düşük faiz demek. Bir de şunu yapın: Bankanızın müşteri temsilcisiyle konuşurken "En iyi oranı verirseniz, başka bankalardan teklif aldığımı söylemem gerekmez" diye esprili bir dille pazarlık yapmayı deneyin. Bazen işe yarıyor!</p>
