                                    <p>Çünkü banka açısından risk daha yüksek. Arsa, içinde yaşanılan bir konut gibi değil, boş bir arazi. Ekonomik bir krizde veya sıkıntıda, arsanın satılması, bir konuta göre daha zor olabilir. Ayrıca, değer artışı daha belirsiz olabilir. Bu risk primi faize yansıyor.</p>
