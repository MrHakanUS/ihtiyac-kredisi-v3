                                    <li><strong>Ön Hazırlık:</strong> Gerekli belgeleri hazırlayın: Kimlik fotokopisi, gelir belgesi (maaş bordrosu, SGK hizmet dökümü, vergi levhası), tapu fotokopisi (eğer arsa seçildiyse), arsanın imar durumunu gösterir belge.</li>
