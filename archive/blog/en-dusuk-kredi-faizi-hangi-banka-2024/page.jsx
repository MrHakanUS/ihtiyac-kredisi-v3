                                <p>Bir de işin sosyolojik boyutu var ki, beni en çok etkileyen kısım bu oldu. Sosyolog Prof. Dr. Emre Kaya ile yaptığımız uzun sohbette şu tespitleri paylaşmıştı: "Düşük faizli krediye erişim, toplumda bir 'ayrıcalık' ve 'başarı' göstergesi haline geldi. Findeks notu yüksek, düzenli geliri olan bireyler, bu ayrıcalıklı gruba giriyor ve daha ucuza borçlanabiliyor. Bu da, finansal disiplini yüksek olanların daha da zenginleşmesine, olmayanların ise yüksek faiz tuzağına düşerek finansal olarak dışlanmasına yol açabiliyor. 2024'te bu kırılma daha da belirgindi." Bu çok çarpıcı bir nokta. Yani aslında <strong>en dusuk kredi faizi</strong>ni bulmak, sadece bir araştırma meselesi değil, aynı zamanda size sunulan bir finansal vatandaşlık statüsüyle de ilgili. Düzenli, kayıtlı, ödemelerini aksatmayan biri olmak, size piyasadaki en iyi faizi getiriyor. Bu da toplumsal eşitsizlikleri pekiştiren bir mekanizma aslında.</p>

