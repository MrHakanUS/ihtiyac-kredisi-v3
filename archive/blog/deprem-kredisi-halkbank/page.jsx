                            <section id="sik-sorulan-sorular">
                                <h2>Sık Sorulan Sorular (SSS)</h2>

                                <h3>Halkbank deprem kredisi için gelir şartı var mı?</h3>
                                <p>
                                    Evet var, ama esnek. Düzenli bir maaşınız olması şart değil. Esnaf veya çiftçiyseniz, gelir belgenizi farklı şekilde (vergi levhası, çiftçi belgesi) sunabilirsiniz. Önemli olan, krediyi geri ödeyebileceğinizi bankaya göstermek.
                                </p>

                                <h3>Kredi başvurusu reddedilirse ne yapmalıyım?</h3>
                                <p>
                                    İlk olarak, reddin nedenini öğrenin. Genellikle yetersiz gelir, yüksek mevcut kredi borcu veya kredi notunun düşük olması başlıca sebepler. Sebebi öğrendikten sonra, durumu düzeltip tekrar başvurabilirsiniz. Alternatif olarak diğer bankaları deneyebilir veya daha düşük tutarlı bir <strong>ihtiyaç kredisi</strong> için başvurmayı düşünebilirsiniz.
                                </p>

                                <h3>Deprem kredisi erken ödenebilir mi? Cezası var mı?</h3>
                                <p>
                                    Evet, erken ödeme yapabilirsiniz. Halkbank'ın deprem kredisinde erken ödeme cezası genellikle uygulanmaz veya sembolik bir tutardır. Ancak yine de sözleşmenizdeki maddeyi kontrol etmeniz en doğrusu.
                                </p>

                                <h3>Birden fazla deprem kredisi kullanabilir miyim?</h3>
                                <p>
                                    Genellikle hayır. Zaten bir deprem kredisi kullandıysanız, ikinci bir başvurunuz onaylanmayabilir. Ancak farklı bir bankadan deneyebilirsiniz, bu durumda her iki kredinin toplam aylık taksitinin gelirinizi aşmamasına dikkat edin.
                                </p>

                                <h3>İhtiyaç kredisi mi yoksa deprem kredisi mi daha avantajlı?</h3>
                                <p>
                                    Kesinlikle deprem kredisi, eğer afet bölgesindeyseniz. Faiz oranları çok daha düşük. Ancak deprem kredisinin kullanım amacı kısıtlıdır (onarım, yeniden inşa vb.). Eğer amaç farklıysa, normal bir <strong>ihtiyaç kredisi</strong> çekmek zorunda kalabilirsiniz.
                                </p>
                            </section>

