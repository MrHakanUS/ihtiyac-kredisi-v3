                                    <li><strong>Finansal Check-Up Yapın:</strong> Geliriniz, gideriniz, birikiminiz, mevcut borçlarınız nedir? Acil durum fonunuz (3-6 aylık gider) oluşturdunuz mu? Yatırıma başlamadan önce bunlar netleşmeli.</li>
