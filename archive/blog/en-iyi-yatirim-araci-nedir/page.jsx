                                    <li><strong>Araştırma Yapın ve Çeşitlendirin:</strong> Tek bir enstrümana tüm paranızı yatırmayın. Mevduat, altın, borsa fonu gibi birkaç farklı kalemde dağıtın. "Yumurtaları aynı sepete koyma" prensibi asla eskimez.</li>
