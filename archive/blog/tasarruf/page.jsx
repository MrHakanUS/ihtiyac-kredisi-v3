                            <section id='sik-sorulan-sorular'>
                                <h2 className='text-2xl font-bold my-4'>Tasarruf ve İhtiyaç Kredisi İlişkisi Hakkında Sık Sorulan Sorular</h2>

                                <div className='space-y-6'>
                                    <div>
                                        <h3 className='text-xl font-semibold'>1. Tasarruf yapmak için önce borçlarımı kapatmalı mıyım?</h3>
                                        <p>Genel kural şu: Eğer bir borcun faizi, tasarruf hesabından elde edeceğiniz faizden yüksekse (ki genelde öyledir), önceliğin o borcu kapatmak olmalı. Örneğin, %30 faizli bir kredi kartı borcun varsa, %20 faizli mevduata para yatırmak mantıklı değil. Önce yüksek maliyetli borcu temizle, sonra birikime başla.</p>
                                    </div>

                                    <div>
                                        <h3 className='text-xl font-semibold'>2. Tasarruf hesabı için en uygun vade ne kadar?</h3>
                                        <p>Bu, paranıza ne zaman ihtiyacın olacağına bağlı. Acil durum fonu gibi likit kalması gereken para kısa vadede (1-3 ay), emeklilik gibi uzun vadeli hedefler için 1 yıl veya daha uzun vade düşünülebilir. Genelde vade uzadıkça faiz oranı da artar. Ancak erken çekmek zorunda kalırsan faiz kaybedersin, dikkatli ol.</p>
                                    </div>

                                    <div>
                                        <h3 className='text-xl font-semibold'>3. Düzenli tasarruf yapmak için otomatik havale nasıl kurulur?</h3>
                                        <p>Çoğu bankanın internet veya mobil şubesinde “otomatik ödeme/havale” talimatı oluşturma seçeneği var. Maaş hesabından, belirlediğin bir tarihte (maaş gününden 1-2 gün sonrası idealdir), belirlediğin tutarın otomatik olarak vadeli hesabına aktarılmasını sağlayabilirsin. Bu, “unuttum” veya “bu ay harcayayım” bahanelerini ortadan kaldırır.</p>
                                    </div>

                                    <div>
                                        <h3 className='text-xl font-semibold'>4. İhtiyaç kredisi çekmek mi yoksa tasarruf etmek mi daha mantıklı?</h3>
                                        <p>Bu, aciliyetine ve maliyetine bağlı. Acil, ertelenemez bir ihtiyaç varsa (sağlık, tamir) ve yeterli birikimin yoksa, ihtiyaç kredisi bir çözüm olabilir. Ancak lüks veya ertelenebilir bir harcama (tatil, yeni cihaz) için kredi çekmek yerine, önce tasarruf edip peşin ödeme yapmak çok daha ucuzdur. Kredi faizi her zaman mevduat faizinden yüksektir.</p>
                                    </div>

                                    <div>
                                        <h3 className='text-xl font-semibold'>5. Tasarruf yaparken vergi ne kadar kesiliyor?</h3>
                                        <p>Mevduat faiz gelirlerinden “stopaj” adı verilen bir vergi kaynağında kesilir. 2026 itibariyle, Türk Lirası mevduatta genellikle %5 oranında stopaj uygulanır. Bazı özel durumlarda (belirli tutarın üzeri) bu oran %10'a çıkabilir. Banka bu vergiyi sizin adınıza keser ve size net faizi öder. Dekontunuzda brüt ve net faiz ayrı yazar.</p>
                                    </div>
                                </div>
                            </section>


