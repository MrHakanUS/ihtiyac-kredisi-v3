                                <p>
                                    Yani, sabırsızsanız ve riske girmek istemiyorsanız hemen teslim mükemmel. Ama bütçeniz daha kısıtlıysa ve esnekliğe ihtiyacınız varsa, inşaat halindeki projeleri de değerlendirin.
                                </p>
