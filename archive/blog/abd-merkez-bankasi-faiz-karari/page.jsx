                            <section>
                                <h2>50.000 TL ve 100.000 TL İçin Detaylı Hesaplama Örnekleri</h2>

                                <p>
                                    Somutlaştıralım. Diyelim ki dayımın oğlu 50.000 TL'ye ihtiyaç duyuyor düğün için. Ya da sen 100.000 TL'ye bir araba peşinatı atacaksın. Fed kararı sonrası ortamda, bu iki popüler tutar için ne öderiz? Hadi hesaplayalım. (Not: Aşağıdaki hesaplamalar, Ocak 2026 ortalaması ve tablodaki oranlar baz alınarak yaklaşık değerlerdir. Kesin rakam için bankaya sor.)
                                </p>

                                <div style={{ margin: '20px 0', padding: '15px', backgroundColor: '#fff0e6', borderLeft: '5px solid #ff9900' }}>
                                    <h3>Örnek 1: 50.000 TL İhtiyaç Kredisi (Vade: 36 Ay)</h3>
                                    <p><strong>Ortalama Faiz Oranı:</strong> %3.0 (yıllık) varsayalım.</p>
                                    <p><strong>Hesaplama:</strong> Kredi tutarı 50.000 TL. Yıllık faiz %3. Aylık faiz = %3 / 12 = %0.25.</p>
                                    <p>Formül biraz karışık olduğu için basit bir online alet kullanmanı öneririm. Ama yaklaşık aylık taksit şöyle bulunur:</p>
                                    <p><strong>Aylık Taksit ≈ [50.000 * (0.0025 * (1+0.0025)^36)] / [ (1+0.0025)^36 - 1 ]</strong></p>
                                    <p>Bu işlemin sonucu yaklaşık <strong>1.550 TL</strong> civarındadır.</p>
                                    <p><strong>Toplam Geri Ödeme:</strong> 1.550 TL * 36 ay = <strong>55.800 TL</strong>.</p>
                                    <p><strong>Toplam Faiz Maliyeti:</strong> 55.800 TL - 50.000 TL = <strong>5.800 TL</strong>.</p>
                                    <p>Yani 3 yılda, 50 bin lira için neredeyse 6 bin lira faiz ödüyorsun. Fed faiz indirirse, belki bu faiz %2.5'e düşer. O zaman aylık taksitin 1.500 TL, toplam faiz maliyetin de 4.800 TL olur. Arada 1.000 TL kazanırsın. Az mı? Bence değil.</p>
                                </div>

                                <div style={{ margin: '20px 0', padding: '15px', backgroundColor: '#e6fff2', borderLeft: '5px solid #00cc66' }}>
                                    <h3>Örnek 2: 100.000 TL İhtiyaç Kredisi (Vade: 24 Ay)</h3>
                                    <p><strong>Ortalama Faiz Oranı:</strong> %3.5 (yıllık) varsayalım.</p>
                                    <p><strong>Aylık Faiz:</strong> %3.5 / 12 = ~%0.2917.</p>
                                    <p><strong>Aylık Taksit Yaklaşık:</strong> <strong>4.470 TL</strong>.</p>
                                    <p><strong>Toplam Geri Ödeme:</strong> 4.470 TL * 24 ay = <strong>107.280 TL</strong>.</p>
                                    <p><strong>Toplam Faiz Maliyeti:</strong> 7.280 TL.</p>
                                    <p>Burada vade kısa olduğu için toplam faiz daha düşük görünüyor. Fed'in indirimi burada da etkili olur. %3.0 faizle aylık taksit 4.320 TL'ye, toplam faiz 6.080 TL'ye düşebilir. Yine 1.200 TL'lik bir fark.</p>
                                </div>

                                <p>
                                    Gördüğün gibi, Fed'in aldığı <strong>ABD Merkez Bankası faiz kararı</strong>, aslında binlerce kilometre öteden senin cebinden çıkacak 1000-1200 TL'yi etkileyebiliyor. Bu yüzden takip etmek, anlamaya çalışmak önemli. Ama dediğim gibi, Türkiye'deki bankaların reel maliyetleri daha önemli. Yani TCMB'nin faizi ve enflasyon.
                                </p>
                            </section>

