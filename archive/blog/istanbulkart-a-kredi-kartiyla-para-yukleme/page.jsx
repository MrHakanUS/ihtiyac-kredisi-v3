                                    <li><strong>Herkese Açık Wi-Fi'lar:</strong> Metroda, havaalanında ücretsiz Wi-Fi ağlarında bankacılık işlemi yapmayın. İnternetiniz yoksa, mobil verinizi kullanın daha güvenli.</li>
