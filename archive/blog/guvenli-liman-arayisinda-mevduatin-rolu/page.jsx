                                            <li>Bankalar genelde erken bozmada faiz kaybı yaşatır. Çoğu banka, vadenin dolmasına kalan süreye göre daha düşük bir faiz (örneğin vadesiz faiz oranı) uygular. Diyelim ki erken bozduğunuzda sadece %5 faiz alabildiniz. 6 aylık faiz geliriniz: 20.000 x (5/100) x (180/365) ≈ 493 TL. Vergi sonrası elinize geçen net artış çok az.</li>
