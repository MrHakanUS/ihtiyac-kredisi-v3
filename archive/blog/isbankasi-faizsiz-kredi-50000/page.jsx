                                <p>Öncelikle red nedenini (kredi notu, gelir yetersizliği, belge eksikliği) öğrenin. Kredi notunuzu yükseltmek için mevcut borçlarınızı kapatın, ödemelerinizi düzenli yapın. 3-6 ay sonra tekrar deneyin veya gelirinizi belgeleyecek ek dokümanlar sunun. Alternatif olarak diğer bankaları da deneyebilirsiniz.</p>
