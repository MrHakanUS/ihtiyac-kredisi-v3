                            <section id='sik-sorulan-sorular'>
                                <h2>PTT Kart Başvurusu Hakkında Sık Sorulan Sorular</h2>

                                <h3>PTT Kart borcu nasıl ödenir?</h3>
                                <p>PTT şubelerinden nakit veya PTT kartınızı kullanarak, PTT'nin belirttiği banka hesap numarasına EFT/havale yaparak veya internetten ödeme yapabilirsiniz. Ekstre döneminde size bir ödeme bilgisi iletilecek.</p>

                                <h3>PTT Kart ihtiyaç kredisi gibi taksitlendirilebilir mi?</h3>
                                <p>Kartın kullanım şekline bağlı. Doğrudan nakit avans çektiyseniz, genelde tek seferlik veya kısa vadeli bir geri ödeme planı sunulur. POS'ta taksitli alışveriş yapmanıza izin veren işyerlerinde, alışverişinizi taksitlendirebilirsiniz. Ancak bu, klasik bir banka kredisindeki gibi 36 aya varan esnek taksitlendirme değildir.</p>

                                <h3>Başvuru reddedilirse ne olur?</h3>
                                <p>Reddedilme nedeninizi genellikle size açıklamazlar. Olası nedenler: yetersiz veya belgelenemeyen gelir, çok yüksek mevcut borçlar veya ciddi kredi geçmişi problemleri olabilir. Red alırsanız, 3-6 ay sonra gelir durumunuz düzelmişse veya borçlarınızı azaltmışsanız tekrar deneyebilirsiniz.</p>

                                <h3>Kart ücreti var mı?</h3>
                                <p>Evet, genelde bir kart temin ücreti (bir kerelik) ve yıllık bir kart kullanım ücreti olabiliyor. 2025'te bu ücretler 50-150 TL bandında değişiyor. Başvuru anında bu ücretleri mutlaka sorun.</p>

                                <h3>PTT Kart ile bankamatiklerden para çekilir mi?</h3>
                                <p>Evet, PTT kartınızı tüm bankamatiklerde (ATM'lerde) kullanabilirsiniz. Ancak unutmayın, PTT dışındaki bir bankamatikten çekim yaparsanız, o banka da ek bir işlem ücreti kesebilir. En az maliyetli çekim PTT bankamatiklerinden yapılandır.</p>
                            </section>


