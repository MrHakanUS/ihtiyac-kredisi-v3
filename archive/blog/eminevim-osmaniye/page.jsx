                                <p><strong>C:</strong> Öncelikle nedenini öğrenin. Banka size resmi bir gerekçe sunmak zorunda. En yaygın nedenler: düşük kredi notu, yetersiz gelir, yüksek mevcut kredi borçları. Kredi notunuzu yükseltmek için (Kredi Kayıt Bürosu'ndan ücretsiz öğrenebilirsiniz) küçük tutarlı kredileri düzenli ödeyin, kredi kartı borçlarınızı kontrol altına alın. 3-6 ay sonra tekrar deneyin.</p>
