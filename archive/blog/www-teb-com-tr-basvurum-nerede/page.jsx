                                <p>Evet, genellikle erken kapatabilirsiniz. Ancak çoğu banka erken kapatma cezası alır. Bu ceza, kalan anaparanın belirli bir yüzdesi (örneğin %1-3) şeklinde olabilir. Sözleşmenizde bu madde mutlaka yazar. Eğer erken kapatmayı düşünüyorsanız, ceza miktarını hesaplayıp, gerçekten karlı mı diye bakın.</p>

