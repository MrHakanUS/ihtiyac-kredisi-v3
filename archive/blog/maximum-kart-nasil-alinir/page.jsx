                                <div className='space-y-6'>
                                    <div>
                                        <h3 className='text-lg font-semibold'>Kredi notum düşük, maximum kart nasıl alınır benim gibi biri için?</h3>
                                        <p>Öncelikle kredi notunuzu yükseltme yoluna gitmelisiniz. Kredi notu 600'ün altındaysa, küçük tutarlı bir ihtiyaç kredisi çekip düzenli ödeme yapmak veya mevcut borçlarınızın taksitlerini aksatmamak en etkili yoldur. Bazı bankalar düşük notu yüksek gelirle telafi edebilir ama bu daha zor bir yol. Sabırlı olun.</p>
                                    </div>

                                    <div>
                                        <h3 className='text-lg font-semibold'>Maximum kart için gelir şartı nedir?</h3>
                                        <p>Net bir asgari gelir şartı yoktur, bankanın risk algısına bağlıdır. Ancak genel kanı asgari ücretin 2-3 katı bir gelirin başvuruyu güçlendirdiği yönünde. Düzensiz geliri olan esnaf için vergi levhası ve banka hesap hareketleri çok önemli. Banka düzenli para girişi görürse, bordrosuz da onay verebilir.</p>
                                    </div>

                                    <div>
                                        <h3 className='text-lg font-semibold'>Maaşım bir bankada, maximum kart için başka bir bankaya başvursam sorun olur mu?</h3>
                                        <p>Olmaz, ancak maaş aldığınız banka sizin finansal düzeninizi bildiği için oradaki şansınız daha yüksektir. Diğer banka sizden daha fazla belge isteyebilir ve daha temkinli yaklaşabilir. İki bankaya aynı anda başvurmak ise kredi raporunuza çok sorgu düşeceği için hiç önermem.</p>
                                    </div>

                                    <div>
                                        <h3 className='text-lg font-semibold'>Maximum kartı aldım, limiti nasıl arttırabilirim?</h3>
                                        <p>Kartı düzenli ve fazla aşım yapmadan kullanın, ödemelerinizi zamanında yapın. Bankanın diğer ürünlerini (mobil uygulama, birikim hesabı) kullanın. Zamanla banka otomatik limit artırımı yapar. Eğer yapmazsa, 6-12 ay sonra gelir belgelerinizi güncelleyerek müşteri hizmetlerinden veya şubeden limit artırım talebinde bulunabilirsiniz.</p>
                                    </div>

                                    <div>
                                        <h3 className='text-lg font-semibold'>Maximum kartın yıllık ücreti yüksek mi? İptal ettirebilir miyim?</h3>
                                        <p>Evet, genelde standart kartlara göre daha yüksek yıllık kart ücreti veya aidatı olur (500-1500 TL bandı). Ancak birçok banka, belirli bir harcama tutarına ulaşıldığında bu ücreti iade ediyor ya da mil kazandırıyor. Kartı aldıktan sonra ücreti size uygun değilse, genelde ilk yıl sonunda ücretsiz bir başka karta geçiş yapabilir veya kartı iptal ettirebilirsiniz. İptal etmeden önce bankayla görüşüp ücret muafiyeti talep etmeyi deneyin.</p>
                                    </div>
                                </div>
